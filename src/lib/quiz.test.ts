import { changeColumns, changeRows } from "./quiz";
import { Table } from "./sudoku";

const sample = [
  [6,4,9,5,1,7,8,2,3],
  [3,7,5,8,2,4,1,6,9],
  [8,2,1,9,3,6,7,5,4],
  [2,9,6,1,4,5,3,8,7],
  [5,1,8,6,7,3,4,9,2],
  [4,3,7,2,9,8,5,1,6],
  [1,5,3,7,6,2,9,4,8],
  [9,6,4,3,8,1,2,7,5],
  [7,8,2,4,5,9,6,3,1],
]

test.each([
  [
    sample,
    1,
    2,
    [
      [6,4,9,5,1,7,8,2,3],
      [8,2,1,9,3,6,7,5,4],
      [3,7,5,8,2,4,1,6,9],
      [2,9,6,1,4,5,3,8,7],
      [5,1,8,6,7,3,4,9,2],
      [4,3,7,2,9,8,5,1,6],
      [1,5,3,7,6,2,9,4,8],
      [9,6,4,3,8,1,2,7,5],
      [7,8,2,4,5,9,6,3,1],
    ]
  ],
  [
    sample,
    1,
    8,
    [
      [6,4,9,5,1,7,8,2,3],
      [7,8,2,4,5,9,6,3,1],
      [8,2,1,9,3,6,7,5,4],
      [2,9,6,1,4,5,3,8,7],
      [5,1,8,6,7,3,4,9,2],
      [4,3,7,2,9,8,5,1,6],
      [1,5,3,7,6,2,9,4,8],
      [9,6,4,3,8,1,2,7,5],
      [3,7,5,8,2,4,1,6,9],
    ]
  ]
])("changeRows %d %d", (given: Table, i: number, j: number, expected: Table) => {
  expect(changeRows(given, i, j)).toEqual(expected);
})

